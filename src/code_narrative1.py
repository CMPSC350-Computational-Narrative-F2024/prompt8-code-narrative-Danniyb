# This code is a simple example of a function that takes a list of numbers and returns the sum of the numbers in the list.

def sum_list(numbers):
    total = 0
    for number in numbers:
        total += number
    return total

numbers = [1, 2, 3, 4, 5]

result = sum_list(numbers)
print(result)

